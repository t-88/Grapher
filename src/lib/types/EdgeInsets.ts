
class EdgeInsets {
    left: number = 0;
    right: number = 0;
    top: number = 0;
    bottom: number = 0;

    constructor(left: number, right: number, top: number, bottom: number) {
        this.left = left;
        this.right = right;
        this.top = top;
        this.bottom = bottom;
    }
}


export default EdgeInsets;